export function getArchiveForZone(zone) {
  return ARCHIVE_DATA[zone] || null;
}

export function getArchiveStats(archive) {
  if (!archive) return null;
  const total = archive.byYear.reduce((s, d) => s + d.alerts, 0);
  const peakYear = archive.byYear.reduce((a, b) => a.alerts > b.alerts ? a : b).label;
  const peakHour = archive.byHour.reduce((a, b) => a.alerts > b.alerts ? a : b).label;
  return { total, peakYear, peakHour };
}
